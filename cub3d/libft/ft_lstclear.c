#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*index;

	if (!lst)
		return ;
	index = *lst;
	while (*lst)
	{
		index = (*lst)->next;
		del((*lst)->content);
		free(*lst);
		*lst = index;
	}
	*lst = NULL;
}
